
-- Drop tables if they already exist
DROP TABLE IF EXISTS EMP;
DROP TABLE IF EXISTS DEPT;
DROP TABLE IF EXISTS BONUS;
DROP TABLE IF EXISTS SALGRADE;

-- DEPT table
CREATE TABLE DEPT (
  DEPTNO INT PRIMARY KEY,
  DNAME VARCHAR(14),
  LOC VARCHAR(13)
);

-- EMP table
CREATE TABLE EMP (
  EMPNO INT PRIMARY KEY,
  ENAME VARCHAR(10),
  JOB VARCHAR(9),
  MGR INT,
  HIREDATE DATE,
  SAL DECIMAL(7,2),
  COMM DECIMAL(7,2),
  DEPTNO INT,
  FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO),
  FOREIGN KEY (MGR) REFERENCES EMP(EMPNO)
);

-- BONUS table
CREATE TABLE BONUS (
  ENAME VARCHAR(10),
  JOB VARCHAR(9),
  SAL DECIMAL(7,2),
  COMM DECIMAL(7,2)
);

-- SALGRADE table
CREATE TABLE SALGRADE (
  GRADE INT,
  LOSAL INT,
  HISAL INT
);

